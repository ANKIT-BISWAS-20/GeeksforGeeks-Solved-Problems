// User function template for C++
class Solution {
  public:
    // Function returns the second
    // largest elements
    int print2largest(vector<int> &arr) {
        int max = arr[0];
        for (auto i : arr){
            if (i > max) max = i;
        }
        int second_max = -1;
        for (auto j : arr){
            if (j>second_max && j<max) second_max = j;
        }
        return second_max;
    }
};

